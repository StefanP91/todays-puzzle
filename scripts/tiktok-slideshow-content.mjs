/** @param {string} lang */
export function getTikTokContent(lang) {
  const content = TIKTOK_CONTENT[lang];
  if (!content) {
    throw new Error(`Unknown TikTok slideshow language: ${lang}`);
  }
  return content;
}

/** @param {string} lang */
export function buildSlides(lang) {
  const c = getTikTokContent(lang);
  return [
    {
      type: "custom",
      lines: [c.gameTitle, c.titleLine2],
      sub: c.titleSub,
    },
    { type: "game", scene: "partial", puzzle: 847, subtitle: c.guessWord },
    { type: "game", scene: "progress", puzzle: 847, subtitle: c.colorHint },
    { type: "game", scene: "won", puzzle: 847, subtitle: c.wonLine },
    {
      type: "custom",
      lines: [c.ctaLine1, c.ctaLine2],
      sub: c.ctaSub,
    },
  ];
}
